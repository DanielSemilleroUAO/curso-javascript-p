// Variables que se pueden modificar su valor
let nombre;
nombre = "Juan";
console.log(nombre);

// Variables que no modifica su valor
const apellido = "Perez";
console.log(apellido);

// Buenas practicas para declarar variables
let nombreCompleto = "Daniel Delgado";
console.log(nombreCompleto);

let x, y;
x = 10, y = 20;
let z = x + y;
console.log(z);